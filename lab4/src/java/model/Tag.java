package model;

/**
 *
 * @author duynh
 */
public class Tag {
    private int tid;
    private String tname;

    public Tag() {
    }

    public Tag(int tid, String tname) {
        this.tid = tid;
        this.tname = tname;
    }

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }
    
    
}
